    public function destroy(Request $request)
    {
        try{
            Storage::disk('upload_attachments')->delete('attachments/'.$request->album_id.'/'.$request->picture_name);
            Picture::where('id',$request->id)->delete();
            toastr()->success("Picture Deleted Successfully");
            return redirect()->back();
        } catch (\Exception $e) {
            toastr()->error($e->getMessage());
            return redirect()->back();
        }
       
